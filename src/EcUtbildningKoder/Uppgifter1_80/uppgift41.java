package EcUtbildningKoder.Uppgifter1_80;

public class uppgift41 {
    public static void main(String[] args) {
        int num = 1;

        int total = 0;

        while (num <= 50) {
            total = total + num;
            num = num + 1;
        }
        System.out.println(total);


    }
}
